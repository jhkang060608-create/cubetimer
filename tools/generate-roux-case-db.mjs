#!/usr/bin/env node
import fs from "fs";
import path from "path";
import { fileURLToPath, pathToFileURL } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");

function parseArgs(argv) {
  const opts = {
    output: path.join(rootDir, "solver", "rouxCaseDb.js"),
    augmentInput: "",
    augmentLimit: 0,
    augmentOffset: 0,
    augmentDeadlineMs: 15000,
    augmentUniqueScrambles: true,
    progressEvery: 10,
    help: false,
  };

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--help" || arg === "-h") {
      opts.help = true;
      continue;
    }

    const [flag, inlineValue] = arg.split("=", 2);
    const value = inlineValue !== undefined ? inlineValue : argv[i + 1];
    const consumeNext = inlineValue === undefined;

    if (flag === "--output") {
      opts.output = path.resolve(rootDir, value || opts.output);
      if (consumeNext) i += 1;
    } else if (flag === "--augment-input") {
      opts.augmentInput = path.resolve(rootDir, value || opts.augmentInput);
      if (consumeNext) i += 1;
    } else if (flag === "--augment-limit") {
      opts.augmentLimit = Number(value);
      if (consumeNext) i += 1;
    } else if (flag === "--augment-offset") {
      opts.augmentOffset = Number(value);
      if (consumeNext) i += 1;
    } else if (flag === "--augment-deadline-ms") {
      opts.augmentDeadlineMs = Number(value);
      if (consumeNext) i += 1;
    } else if (flag === "--augment-unique-scrambles") {
      opts.augmentUniqueScrambles = value !== "0" && String(value).toLowerCase() !== "false";
      if (consumeNext) i += 1;
    } else if (flag === "--progress-every") {
      opts.progressEvery = Number(value);
      if (consumeNext) i += 1;
    }
  }

  return opts;
}

function printHelp() {
  console.log("Usage: node tools/generate-roux-case-db.mjs [options]");
  console.log("");
  console.log("Builds the offline Roux FB/SB/CMLL/LSE case DB used by solver/roux3x3.js.");
  console.log("");
  console.log("Options:");
  console.log("  --output <path>                  Output JS module");
  console.log("  --augment-input <path>           Optional details JSON to harvest extra FB/SB cases");
  console.log("  --augment-limit <n>              Number of records to harvest (0 = all)");
  console.log("  --augment-offset <n>             Record offset before limiting");
  console.log("  --augment-deadline-ms <n>        Per-record phase-recovery budget (default: 15000)");
  console.log("  --augment-unique-scrambles <b>   Deduplicate by scramble (default: true)");
  console.log("  --progress-every <n>             Print progress every N harvested records");
}

function buildOutput(artifacts) {
  const payload = JSON.stringify(artifacts, null, 2);
  const lines = [];
  lines.push("// Auto-generated by tools/generate-roux-case-db.mjs");
  lines.push(`// Generated at: ${artifacts.generatedAt || new Date().toISOString()}`);
  lines.push("");
  lines.push(`export const ROUX_CASE_DB = Object.freeze(${payload});`);
  lines.push("");
  return lines.join("\n");
}

function countSerializedKeys(table) {
  return table && typeof table === "object" ? Object.keys(table).length : 0;
}

function toNonNegativeInt(value, fallback) {
  return Number.isFinite(value) && value >= 0 ? Math.floor(value) : fallback;
}

function loadRecords(inputPath) {
  if (!inputPath) return [];
  const payload = JSON.parse(fs.readFileSync(inputPath, "utf8"));
  if (Array.isArray(payload)) return payload;
  if (Array.isArray(payload?.records)) return payload.records;
  throw new Error(`Unsupported augmentation input shape: ${inputPath}`);
}

function selectRecords(records, options) {
  const deduped = [];
  const seenScrambles = new Set();
  for (let i = 0; i < records.length; i++) {
    const record = records[i];
    const scramble = String(record?.scramble || "").trim();
    if (!scramble) continue;
    if (options.augmentUniqueScrambles) {
      if (seenScrambles.has(scramble)) continue;
      seenScrambles.add(scramble);
    }
    deduped.push(record);
  }
  const offset = toNonNegativeInt(options.augmentOffset, 0);
  const limit = toNonNegativeInt(options.augmentLimit, 0);
  return deduped.slice(offset, limit > 0 ? offset + limit : undefined);
}

function mergeSerializedCandidate(table, key, text, maxPerKey = 8) {
  const resolvedKey = String(key || "").trim();
  const resolvedText = String(text || "").trim();
  if (!resolvedKey || !resolvedText) return false;
  const bucket = Array.isArray(table[resolvedKey]) ? table[resolvedKey].slice() : [];
  if (bucket.includes(resolvedText)) return false;
  bucket.push(resolvedText);
  bucket.sort((a, b) => {
    const aLen = a ? a.split(/\s+/).filter(Boolean).length : 0;
    const bLen = b ? b.split(/\s+/).filter(Boolean).length : 0;
    return aLen - bLen || a.localeCompare(b);
  });
  if (bucket.length > maxPerKey) {
    bucket.length = maxPerKey;
  }
  table[resolvedKey] = bucket;
  return true;
}

let getDefaultPatternFn = null;
let buildRouxCaseDbArtifactsFn = null;
let buildRouxAugmentationCandidatesFromPatternFn = null;

async function loadSolverExports() {
  const contextPath = path.join(rootDir, "solver", "context.js");
  const rouxPath = path.join(rootDir, "solver", "roux3x3.js");
  const [ctxMod, rouxMod] = await Promise.all([
    import(pathToFileURL(contextPath).href),
    import(pathToFileURL(rouxPath).href),
  ]);
  getDefaultPatternFn = ctxMod.getDefaultPattern || ctxMod.default?.getDefaultPattern;
  buildRouxCaseDbArtifactsFn = rouxMod.buildRouxCaseDbArtifacts || rouxMod.default?.buildRouxCaseDbArtifacts;
  buildRouxAugmentationCandidatesFromPatternFn =
    rouxMod.buildRouxAugmentationCandidatesFromPattern || rouxMod.default?.buildRouxAugmentationCandidatesFromPattern;
  if (typeof buildRouxCaseDbArtifactsFn !== "function") {
    throw new Error("buildRouxCaseDbArtifacts not found in solver/roux3x3.js");
  }
}

async function applyAugmentation(artifacts, options) {
  if (!options.augmentInput) {
    return {
      enabled: false,
      totalSelected: 0,
      processed: 0,
      fbAdded: 0,
      sbAdded: 0,
      candidatePlans: 0,
      skipped: 0,
      errors: {},
    };
  }

  const allRecords = loadRecords(options.augmentInput);
  const selectedRecords = selectRecords(allRecords, options);
  const solved = await getDefaultPatternFn("333");
  const deadlineMs = Math.max(1500, toNonNegativeInt(options.augmentDeadlineMs, 15000));
  const progressEvery = Math.max(1, toNonNegativeInt(options.progressEvery, 10));

  const stats = {
    enabled: true,
    input: options.augmentInput,
    totalInputRecords: allRecords.length,
    totalSelected: selectedRecords.length,
    processed: 0,
    fbAdded: 0,
    sbAdded: 0,
    candidatePlans: 0,
    skipped: 0,
    errors: {},
  };

  for (let i = 0; i < selectedRecords.length; i++) {
    const record = selectedRecords[i];
    const scramble = String(record?.scramble || "").trim();
    if (!scramble) {
      stats.skipped += 1;
      continue;
    }

    const pattern = solved.applyAlg(scramble);
    const candidateResult = await buildRouxAugmentationCandidatesFromPatternFn(pattern, {
      deadlineTs: Date.now() + deadlineMs,
      tryAlternate: true,
      collectAllCandidates: true,
      maxCandidates: 6,
    }).catch((error) => ({
      ok: false,
      reason: String(error?.code || error?.message || "ROUX_AUGMENT_ERROR"),
      candidates: [],
      errors: [],
    }));

    stats.processed += 1;
    stats.candidatePlans += Array.isArray(candidateResult?.candidates)
      ? candidateResult.candidates.length
      : 0;

    if (Array.isArray(candidateResult?.candidates) && candidateResult.candidates.length) {
      for (let c = 0; c < candidateResult.candidates.length; c++) {
        const candidate = candidateResult.candidates[c];
        const planName = candidate.planName;
        if (candidate.accepted?.fb) {
          const added = mergeSerializedCandidate(
            artifacts.fbByPlan[planName] || (artifacts.fbByPlan[planName] = {}),
            candidate.fb?.key,
            candidate.fb?.text,
            8,
          );
          if (added) stats.fbAdded += 1;
        }
        if (candidate.accepted?.sb) {
          const added = mergeSerializedCandidate(
            artifacts.sbByPlan[planName] || (artifacts.sbByPlan[planName] = {}),
            candidate.sb?.key,
            candidate.sb?.text,
            8,
          );
          if (added) stats.sbAdded += 1;
        }
      }
    } else {
      stats.skipped += 1;
      const reason = String(candidateResult?.reason || candidateResult?.errors?.[0]?.reason || "NO_CANDIDATE");
      stats.errors[reason] = (stats.errors[reason] || 0) + 1;
    }

    if ((i + 1) % progressEvery === 0 || i === selectedRecords.length - 1) {
      console.log(
        `[augment] ${i + 1}/${selectedRecords.length} processed | fbAdded=${stats.fbAdded} sbAdded=${stats.sbAdded} skipped=${stats.skipped}`,
      );
    }
  }

  return stats;
}

function refreshMetaCounts(artifacts) {
  const planNames = new Set([
    ...Object.keys(artifacts.fbByPlan || {}),
    ...Object.keys(artifacts.sbByPlan || {}),
  ]);
  artifacts.meta = artifacts.meta && typeof artifacts.meta === "object" ? artifacts.meta : {};
  artifacts.meta.plans = artifacts.meta.plans && typeof artifacts.meta.plans === "object"
    ? artifacts.meta.plans
    : {};

  for (const planName of planNames) {
    const planMeta = artifacts.meta.plans[planName] && typeof artifacts.meta.plans[planName] === "object"
      ? artifacts.meta.plans[planName]
      : {};
    const fbMeta = planMeta.fb && typeof planMeta.fb === "object" ? planMeta.fb : {};
    const sbMeta = planMeta.sb && typeof planMeta.sb === "object" ? planMeta.sb : {};
    fbMeta.keyCount = countSerializedKeys(artifacts.fbByPlan?.[planName]);
    sbMeta.keyCount = countSerializedKeys(artifacts.sbByPlan?.[planName]);
    planMeta.fb = fbMeta;
    planMeta.sb = sbMeta;
    artifacts.meta.plans[planName] = planMeta;
  }

  if (!artifacts.meta.cmll || typeof artifacts.meta.cmll !== "object") {
    artifacts.meta.cmll = {};
  }
  if (!artifacts.meta.lse || typeof artifacts.meta.lse !== "object") {
    artifacts.meta.lse = {};
  }
  artifacts.meta.cmll.keyCount = countSerializedKeys(artifacts.cmll);
  artifacts.meta.lse.keyCount = countSerializedKeys(artifacts.lse);
}

async function main() {
  const opts = parseArgs(process.argv.slice(2));
  if (opts.help) {
    printHelp();
    return;
  }
  await loadSolverExports();

  const startedAt = Date.now();
  const artifacts = await buildRouxCaseDbArtifactsFn();
  const augmentation = await applyAugmentation(artifacts, opts);
  refreshMetaCounts(artifacts);
  artifacts.generatedAt = new Date().toISOString();
  artifacts.meta = artifacts.meta && typeof artifacts.meta === "object" ? artifacts.meta : {};
  artifacts.meta.augmentation = augmentation;
  const outputCode = buildOutput(artifacts);
  fs.writeFileSync(opts.output, outputCode, "utf8");

  const elapsedMs = Date.now() - startedAt;
  console.log(`Wrote ${opts.output}`);
  console.log(`Generated in ${elapsedMs}ms`);
  console.log(
    [
      `FB LEFT_FIRST keys=${countSerializedKeys(artifacts.fbByPlan?.LEFT_FIRST)}`,
      `FB RIGHT_FIRST keys=${countSerializedKeys(artifacts.fbByPlan?.RIGHT_FIRST)}`,
      `SB LEFT_FIRST keys=${countSerializedKeys(artifacts.sbByPlan?.LEFT_FIRST)}`,
      `SB RIGHT_FIRST keys=${countSerializedKeys(artifacts.sbByPlan?.RIGHT_FIRST)}`,
      `CMLL keys=${countSerializedKeys(artifacts.cmll)}`,
      `LSE keys=${countSerializedKeys(artifacts.lse)}`,
    ].join(" | "),
  );
  if (augmentation.enabled) {
    console.log(
      `Augmentation: selected=${augmentation.totalSelected} fbAdded=${augmentation.fbAdded} sbAdded=${augmentation.sbAdded} skipped=${augmentation.skipped}`,
    );
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
