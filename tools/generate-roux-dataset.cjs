#!/usr/bin/env node
const fs = require("fs");
const path = require("path");
const vm = require("vm");

const rootDir = path.resolve(__dirname, "..");
const sourcePath = path.join(rootDir, "vendor-data", "alg-trainer", "js", "alg_list.js");
const outputPath = path.join(rootDir, "solver", "rouxDataset.js");

function readSourceFile(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Source file not found: ${filePath}`);
  }
  return fs.readFileSync(filePath, "utf8");
}

function loadContext(sourceCode) {
  const context = {};
  context.window = context;
  vm.createContext(context);
  vm.runInContext(sourceCode, context, { timeout: 30000 });
  return context;
}

function sanitizeVariant(raw) {
  let text = String(raw || "").replace(/\s+/g, " ").trim();
  if (!text) return "";
  text = text.replace(/^\((U2|U'|U)\)\s*/i, "");
  text = text.replace(/^\((U2|U'|U)\)\s*/i, "");
  text = text.replace(/[()]/g, " ");
  text = text.replace(/^\/+|\/+$/g, "");
  text = text.replace(/\s+/g, " ").trim();
  if (!text) return "";
  if (!/^[URFDLBMESXYZurfdlbmesxyzw'2\s]+$/.test(text)) return "";
  return text;
}

function splitVariants(rawAlg) {
  const out = [];
  const seen = new Set();
  const variants = String(rawAlg || "").split("/");
  for (let i = 0; i < variants.length; i++) {
    const normalized = sanitizeVariant(variants[i]);
    if (!normalized || seen.has(normalized)) continue;
    seen.add(normalized);
    out.push(normalized);
  }
  return out;
}

function flattenObjectAlgorithms(obj) {
  const result = [];
  if (!obj || typeof obj !== "object") return result;
  for (const value of Object.values(obj)) {
    if (!Array.isArray(value)) continue;
    for (let i = 0; i < value.length; i++) {
      const variants = splitVariants(value[i]);
      for (let j = 0; j < variants.length; j++) {
        result.push(variants[j]);
      }
    }
  }
  return result;
}

function dedupePreserveOrder(list) {
  const seen = new Set();
  const out = [];
  for (let i = 0; i < list.length; i++) {
    const item = list[i];
    if (!item || seen.has(item)) continue;
    seen.add(item);
    out.push(item);
  }
  return out;
}

function buildOutput(cmll, lse) {
  const generatedAt = new Date().toISOString();
  const lines = [];
  lines.push("// Auto-generated by tools/generate-roux-dataset.cjs");
  lines.push("// Source: vendor-data/alg-trainer/js/alg_list.js");
  lines.push(`// Generated at: ${generatedAt}`);
  lines.push("");
  lines.push("export const ROUX_FORMULAS = Object.freeze({");
  lines.push(`  CMLL: ${JSON.stringify(cmll, null, 2)},`);
  lines.push(`  LSE: ${JSON.stringify(lse, null, 2)},`);
  lines.push("});");
  lines.push("");
  lines.push("export const ROUX_FORMULA_COUNTS = Object.freeze({");
  lines.push(`  CMLL: ${cmll.length},`);
  lines.push(`  LSE: ${lse.length},`);
  lines.push("});");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const sourceCode = readSourceFile(sourcePath);
  const ctx = loadContext(sourceCode);

  const cmllRaw = []
    .concat(flattenObjectAlgorithms(ctx.CMLL))
    .concat(flattenObjectAlgorithms(ctx.oh_cmll));
  const lseRaw = []
    .concat(flattenObjectAlgorithms(ctx.l5e_full))
    .concat(flattenObjectAlgorithms(ctx.L5E))
    .concat(flattenObjectAlgorithms(ctx.alg_2lookl5efreo))
    .concat(flattenObjectAlgorithms(ctx.ELL));

  const cmll = dedupePreserveOrder(cmllRaw);
  const lse = dedupePreserveOrder(lseRaw);

  const outputCode = buildOutput(cmll, lse);
  fs.writeFileSync(outputPath, outputCode, "utf8");
  console.log(`Wrote ${outputPath}`);
  console.log(`CMLL formulas: ${cmll.length}, LSE formulas: ${lse.length}`);
}

main();
