#!/usr/bin/env node
const fs = require("fs");
const path = require("path");
const vm = require("vm");

const rootDir = path.resolve(__dirname, "..");
const sourcePath = path.join(rootDir, "vendor-data", "alg-trainer", "js", "alg_list.js");
const outputPath = path.join(rootDir, "solver", "zbDataset.js");

function readSourceFile(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Source file not found: ${filePath}`);
  }
  return fs.readFileSync(filePath, "utf8");
}

function loadDatasetObjects(sourceCode) {
  const context = {};
  context.window = context;
  vm.createContext(context);
  vm.runInContext(sourceCode, context, { timeout: 20000 });
  return {
    zbls: context.ZBLS || {},
    zbll: context.zbll_full || {},
  };
}

function normalizeVariant(raw) {
  let text = String(raw || "").replace(/\s+/g, " ").trim();
  if (!text) return "";

  // Remove common leading AUF annotation used in this dataset.
  text = text.replace(/^\((U2|U'|U)\)\s*/i, "");
  text = text.replace(/^\((U2|U'|U)\)\s*/i, "");

  // Some entries carry trailing slash delimiters.
  text = text.replace(/^\/+|\/+$/g, "").trim();
  return text;
}

function flattenAlgorithms(caseMap) {
  const result = [];
  const seen = new Set();
  for (const value of Object.values(caseMap || {})) {
    if (!Array.isArray(value)) continue;
    for (const rawAlg of value) {
      const variants = String(rawAlg || "").split("/");
      for (const variant of variants) {
        const normalized = normalizeVariant(variant);
        if (!normalized) continue;
        if (seen.has(normalized)) continue;
        seen.add(normalized);
        result.push(normalized);
      }
    }
  }
  return result;
}

function buildOutput(zblsList, zbllList) {
  const generatedAt = new Date().toISOString();
  const lines = [];
  lines.push("// Auto-generated by tools/generate-zb-dataset.cjs");
  lines.push("// Source: vendor-data/alg-trainer/js/alg_list.js");
  lines.push(`// Generated at: ${generatedAt}`);
  lines.push("");
  lines.push("export const ZB_FORMULAS = Object.freeze({");
  lines.push(`  ZBLS: ${JSON.stringify(zblsList, null, 2)},`);
  lines.push(`  ZBLL: ${JSON.stringify(zbllList, null, 2)},`);
  lines.push("});");
  lines.push("");
  lines.push("export const ZB_FORMULA_COUNTS = Object.freeze({");
  lines.push(`  ZBLS: ${zblsList.length},`);
  lines.push(`  ZBLL: ${zbllList.length},`);
  lines.push("});");
  lines.push("");
  return lines.join("\n");
}

function main() {
  const sourceCode = readSourceFile(sourcePath);
  const { zbls, zbll } = loadDatasetObjects(sourceCode);
  const zblsList = flattenAlgorithms(zbls);
  const zbllList = flattenAlgorithms(zbll);
  const outputCode = buildOutput(zblsList, zbllList);
  fs.writeFileSync(outputPath, outputCode, "utf8");
  console.log(`Wrote ${outputPath}`);
  console.log(`ZBLS: ${zblsList.length} formulas, ZBLL: ${zbllList.length} formulas`);
}

main();
